interface TranslationContextType {
  language: LanguageCode;
  setLanguage: (lang: LanguageCode) => void;
  t: (key: string) => string;
}

const TranslationContext = createContext<TranslationContextType | undefined>(undefined);

export const TranslationProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<LanguageCode>("en");

  // Sync dir layout with RTL requirements for Arabic and Russian as requested
  // Note: The prompt asks for: "RTL (right-to-left) layout support must be enabled for Arabic and Russian."
  useEffect(() => {
    const isRtl = language === "ar" || language === "ru";
    document.documentElement.dir = isRtl ? "rtl" : "ltr";
    
    // Toggle some rtl styling classes optionally if needed
    if (isRtl) {
      document.documentElement.classList.add("rtl");
    } else {
      document.documentElement.classList.remove("rtl");
    }
  }, [language]);

  const setLanguage = (lang: LanguageCode) => {
    setLanguageState(lang);
  };

  const t = (key: string): string => {
    const dict = translationDictionary[language] || translationDictionary.en;
    return dict[key] || translationDictionary.en[key] || key;
  };

  return (
    <TranslationContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </TranslationContext.Provider>
  );
};

export const useTranslation = () => {
  const context = useContext(TranslationContext);
  if (!context) {
    throw new Error("useTranslation must be used within a TranslationProvider");
  }
  return context;
};
